// Switch statement
const month = "feb";

switch (month) {
    case "jan":
        console.log("January");
        break;

    case "feb":
        console.log("February");
        break;

    case "march":
        console.log("March");
        break;

    case "april":
        console.log("April");
        break;

    default:
        console.log("No matching month found.");
        break;
}
